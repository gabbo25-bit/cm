def solve_bird_spotters(input_data):
    t = 100
    grid_size = 1000
    frame_min = 250
    frame_max = 750 # 250 + 500
    
    birds_in_frame = 0
    lines = input_data.strip().split('\n')
    
    for line in lines:
        if not line.strip(): continue
        vx, vy = map(int, line.split(','))
        
        # Calcolo posizione dopo 100 secondi con wrap-around
        xf = (vx * t) % grid_size
        yf = (vy * t) % grid_size
        
        # Verifica se l'uccello Ã¨ nel frame [250, 750)
        if frame_min <= xf < frame_max and frame_min <= yf < frame_max:
            birds_in_frame += 1
            
    return birds_in_frame

